      protected void datatable(){
        Object[] Baris={"ID Pelanggan","Nama Pelanggan","Jenis Kelamin","No Telepon","Alamat"};//nama kolom tabel di form
        tabmode = new DefaultTableModel(null,Baris);
        String cariitem=txt_cari.getText();
        try{
            String sql="SELECT*FROM pelanggan where id_pelanggan like'%"+cariitem+"%' or nama_pelanggan like '%"+cariitem+"%'order by id_pelanggan asc";
            Statement stat= koneksi.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
            });
            }
            tbl_pelanggan.setModel(tabmode);
        }catch (Exception e){
                JOptionPane.showMessageDialog(null,"data gagal dipanggil"+ e);
        } 
        
        
    }

