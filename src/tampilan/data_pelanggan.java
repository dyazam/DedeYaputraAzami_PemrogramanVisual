    private void rSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSimpanActionPerformed
        String jenis= null;
        if(rLaki.isSelected()){
            jenis = "Laki-laki";
        }else if(rPerempuan.isSelected()){
            jenis = "Perempuan";
        }
        String sql="insert into pelanggan values(?,?,?,?,?)";
        try{
            PreparedStatement stat=koneksi.prepareStatement(sql);
            stat.setString(1, txt_id.getText());
            stat.setString(2, txt_nama.getText());
            stat.setString(3, jenis);
            stat.setString(4, txt_tlp.getText());
            stat.setString(5, txt_alamat.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"data berhasil disimpan");
            kosong();
            txt_id.requestFocus();
        }
         catch (SQLException e){
             JOptionPane.showMessageDialog(null,"data gagal disimpan "+e);
    }
        datatable();
// TODO add your handling code here:
    }//GEN-LAST:event_rSimpanActionPerformed

