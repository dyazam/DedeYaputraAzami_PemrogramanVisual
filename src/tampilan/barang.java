    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
       String sql="insert into barang values(?,?,?,?,?)";
        try{
            PreparedStatement stat=koneksi.prepareStatement(sql);
            stat.setString(1, txt_kd.getText());
            stat.setString(2, txt_nama.getText());
            stat.setString(3, cb_jenis.getSelectedItem().toString());
            stat.setString(4, txt_hargabeli.getText());
            stat.setString(5, txt_hargajual.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"data berhasil disimpan");
            kosong();
            txt_kd.requestFocus();
        }
         catch (SQLException e){
             JOptionPane.showMessageDialog(null,"data gagal disimpan "+e);
    }
        datatable();                                    // TODO add your handling code here:
    }//GEN-LAST:event_bSimpanActionPerformed

