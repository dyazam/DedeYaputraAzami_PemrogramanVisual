    protected void datatable(){
        Object[] Baris={"Kode Barang","Nama Barang","Jenis","Harga Beli","Harga Jual"};//nama kolom tabel di form
        tabmode = new DefaultTableModel(null,Baris);
        String cariitem=txt_cari.getText();
        try{
            String sql="SELECT*FROM barang where kode_barang like'%"+cariitem+"%' or nama_barang like '%"+cariitem+"%'order by kode_barang asc";
            Statement stat= koneksi.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
            });
            }
            tbl_barang.setModel(tabmode);
}catch (Exception e){
    JOptionPane.showMessageDialog(null,"data gagal dipanggil"+ e);
        } 
    }

