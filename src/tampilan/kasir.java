      protected void datatable(){
      Object[] Baris={"ID Kasir","Nama Kasir","Jenis Kelamin","No Telepon","Agama","Alamat","Password"};//nama kolom tabel di form
        tabmode = new DefaultTableModel(null,Baris);
        String cariitem=txt_cari.getText();
        try{
            String sql="SELECT*FROM kasir where id_kasir like'%"+cariitem+"%' or nama_kasir like '%"+cariitem+"%'order by id_kasir asc";
            Statement stat= koneksi.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
                hasil.getString(6),
                hasil.getString(7),
            });
            }
            tbl_kasir.setModel(tabmode);
   }catch (Exception e){
        JOptionPane.showMessageDialog(null,"data gagal dipanggil"+ e);
        } 
    }


 

