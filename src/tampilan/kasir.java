    private void rHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rHapusActionPerformed
    int ok=JOptionPane.showConfirmDialog(null,"hapus","konfirmasi dialog",JOptionPane.YES_NO_OPTION);
         if(ok==0){
             String sql="delete from kasir where id_kasir='"+txt_idkasir.getText()+"'";
             try{
                 PreparedStatement stat =koneksi.prepareStatement(sql);
                 stat.executeUpdate();
                 JOptionPane.showMessageDialog(null,"data berhasil dihapus");
                 kosong();
                 txt_idkasir.requestFocus();
            } 
             catch (SQLException e){
                 JOptionPane.showMessageDialog(null,"data gagal dihapus"+e);           
             }
             datatable();
         }    // TODO add your handling code here:
    }//GEN-LAST:event_rHapusActionPerformed

    private void rSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSimpanActionPerformed
    String jenis=null;
        if(rLaki.isSelected()){
            jenis="Laki-laki";
        }else if(rPerempuan.isSelected()){
            jenis="Perempuan";
        }
        String sql="insert into kasir values(?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat=koneksi.prepareStatement(sql);
            stat.setString(1, txt_idkasir.getText());
            stat.setString(2, txt_nama.getText());
            stat.setString(3, jenis);
            stat.setString(4, txt_tlp.getText());
            stat.setString(5, cb_agama.getSelectedItem().toString());
            stat.setString(6, txt_alamat.getText());
            stat.setString(7, txt_password.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"data berhasil disimpan");
            kosong();
            txt_idkasir.requestFocus();
        }
         catch (SQLException e){
             JOptionPane.showMessageDialog(null,"data gagal disimpan "+e);
    }
        datatable(); // TODO add your handling code here:
    }//GEN-LAST:event_rSimpanActionPerformed

